    public void CarregaDados( ArrayList<model.Classificacao> confrontos )
    {
        
        DefaultTableModel linha = (DefaultTableModel)grid.getModel();
           
        for(model.Classificacao con: confrontos)
        { 
            Object [] dados = {
                con.getTime(),
                con.getPontos(),
                con.getJogos(),
                con.getVitoria(),
                con.getEmpate(),
                con.getDerrota(),
                con.getGolsP(),
                con.getGolsC(),
                con.getSaldo()
            };
            linha.addRow(dados);
        }
            
        
          
    }
    
